CREATE TABLE T_HL_DIC_GENERAL (
ID VARCHAR(32) NOT NULL
, ZDLX VARCHAR(50) NOT NULL
, ZDLXMS VARCHAR(100)
, ZDDM VARCHAR(50) NOT NULL
, XH NUMBER(3)
, ZDMC VARCHAR(100)
, BK1 VARCHAR(200)
, BK2 VARCHAR(200)
, BK3 VARCHAR(200)
, DEL_FLAG VARCHAR(2) DEFAULT '0' NOT NULL
, CREATE_BY VARCHAR(32)
, CREATE_TIME DATETIME(0) DEFAULT SYSDATE NOT NULL
, UPDATE_BY VARCHAR(32)
, UPDATE_TIME DATETIME(0) DEFAULT SYSDATE NOT NULL
, REMARKS VARCHAR(255)
) STORAGE(FILLFACTOR 95, ON TS_HLYTH_KEEP);
ALTER TABLE T_HL_DIC_GENERAL ADD CONSTRAINT  T_HL_DIC_GENERAL_PK PRIMARY KEY (id);
CREATE CLUSTER INDEX T_HL_DIC_GENERAL_CLS ON T_HL_DIC_GENERAL(zdlx, zddm);
COMMENT ON COLUMN T_HL_DIC_GENERAL.ID IS '主键ID';
COMMENT ON COLUMN T_HL_DIC_GENERAL.ZDLX IS '字典类型';
COMMENT ON COLUMN T_HL_DIC_GENERAL.ZDLXMS IS '字典类型描述';
COMMENT ON COLUMN T_HL_DIC_GENERAL.ZDDM IS '字典代码';
COMMENT ON COLUMN T_HL_DIC_GENERAL.XH IS '序号';
COMMENT ON COLUMN T_HL_DIC_GENERAL.ZDMC IS '字典名称';
COMMENT ON COLUMN T_HL_DIC_GENERAL.BK1 IS '保留字段1';
COMMENT ON COLUMN T_HL_DIC_GENERAL.BK2 IS '保留字段2';
COMMENT ON COLUMN T_HL_DIC_GENERAL.BK3 IS '保留字段3';
COMMENT ON COLUMN T_HL_DIC_GENERAL.DEL_FLAG IS '删除标志';
COMMENT ON COLUMN T_HL_DIC_GENERAL.CREATE_BY IS '创建人';
COMMENT ON COLUMN T_HL_DIC_GENERAL.CREATE_TIME IS '创建时间';
COMMENT ON COLUMN T_HL_DIC_GENERAL.UPDATE_BY IS '更新人';
COMMENT ON COLUMN T_HL_DIC_GENERAL.UPDATE_TIME IS '更新时间';
COMMENT ON COLUMN T_HL_DIC_GENERAL.REMARKS IS '备注';
COMMENT ON TABLE T_HL_DIC_GENERAL IS '护理通用字典表';
