COMMENT ON COLUMN T_HL_DIC_ZLS.DEL_FLAG IS '删除标志';
COMMENT ON COLUMN T_HL_DIC_ZLS.CREATE_BY IS '创建人';
COMMENT ON COLUMN T_HL_DIC_ZLS.CREATE_TIME IS '创建时间';
COMMENT ON COLUMN T_HL_DIC_ZLS.UPDATE_BY IS '更新人';
COMMENT ON COLUMN T_HL_DIC_ZLS.UPDATE_TIME IS '更新时间';
